				const owner = getNames.find(e => e.uid === ownerUID);
				const ownerBlock = ` ‚å¨| ${owner?.name || "Unknown"}\n‚ï∞=> ${owner?.uid || "N/A"}`;

				const otherAdmins = getNames.filter(e => e.uid !== ownerUID);
